package com.example.android.dicodingacademy;

public class Academy {
    private String name, remarks, photo, detail, siswa, bonus, level, platform, imgpenyusun, penyusun, link;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public String getRemarks() {
        return remarks;
    }
    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }

    public String getPhoto() {
        return photo;
    }
    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public String getDetail() {
        return detail;
    }
    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getSiswa() {
        return siswa;
    }
    public void setSiswa(String siswa) {
        this.siswa = siswa;
    }

    public String getBonus() {
        return bonus;
    }
    public void setBonus(String bonus) {
        this.bonus = bonus;
    }

    public String getLevel() {
        return level;
    }
    public void setLevel(String level) {
        this.level = level;
    }

    public String getPlatform() {
        return platform;
    }
    public void setPlatform(String platform) {
        this.platform = platform;
    }

    public String getImgpenyusun() {
        return imgpenyusun;
    }
    public void setImgpenyusun(String imgpenyusun) {
        this.imgpenyusun = imgpenyusun;
    }

    public String getPenyusun() {
        return penyusun;
    }
    public void setPenyusun(String penyusun) {
        this.penyusun = penyusun;
    }

    public String getLink() {
        return link;
    }
    public void setLink(String link) {
        this.link = link;
    }
}
